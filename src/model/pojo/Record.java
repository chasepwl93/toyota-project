package model.pojo;

public class Record {

	private int itemNo;
	private String timeStamp;

	public int getItemNo() {
		return itemNo;
	}

	public void setItemNo(int itemNo) {
		this.itemNo = itemNo;
	}

	public String getTimestamp() {
		return timeStamp;
	}

	public void setTimestamp(String timestamp) {
		this.timeStamp = timestamp;
	}

}
